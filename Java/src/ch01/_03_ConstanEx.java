package ch01;

public class _03_ConstanEx {
	public static void main(String[] args) {
		/*
		 * 상수 : 프로그램이 실행되는 동안, 값이 변하지 않는 수 또는 변경불가능한 수
		 *		final 키워드는 변수에 값이 대입되고 나면, 변수가값이 더이상 변경되지 않는다는 으 ㅣ미
		 *		주로 대문자를 사용
		
		*/
		final double PI = 3.14; //상수
		double s = 0.5;
		
		double reslt = PI * s;
		System.out.println(reslt);
		
		
		
	}
	
}	
