package 복습;

public class _01_VariavleEx {
	public static void main(String[] args) {
		/*
		 *  // A : 아스키값 65
		 *  // B : 아스키값 66
		 */
			char ch1 = 'A' ; // 대입
				
		System.out.println(ch1); // aa 변수값 출력
		System.out.println((int)ch1); // 문자에 해당하는 정수값(아스키값) 출력
		
		char bb = 68;
		 
		System.out.println(bb); // 문자출력
		
		int cc = 69;
		
		System.out.println(cc); //문자 정수값 출력
		System.out.println((char)cc); //문자출력
		} // main

}// class
