public class _22_ForEx2 {
	public static void main(String[] args) {
		// 2단부터 9단까지 출력(dan , i)
		for(int dan=2; dan<=9; dan++) {// 3단만 출력하고싶으면 dan<=9를 dan<=3으로 바꾸면 3단만 출력
			System.out.println("------------");
			System.out.println("***"+dan+"단---");
			System.out.println("------------");
			for(int i = 1; i<=9; i++) {
				System.out.println(dan+" * "+i+" = "+ (dan*i));
			}
			System.out.println("------------");
		}	
	}
}
